// 保存初始阵容数据的常量（用于重置功能）
const INITIAL_TEAM_DATA = JSON.parse(JSON.stringify(window.teamData));

// 保存和加载阵容数据
function saveTeamData() {
    localStorage.setItem('teamData', JSON.stringify(window.teamData));
}

function loadTeamData() {
    const saved = localStorage.getItem('teamData');
    if (saved) {
        window.teamData = JSON.parse(saved);
    }
}

// 获取初始阵容数据（用于重置功能）
function getInitialTeamData() {
    // 返回初始数据的深拷贝，避免被修改
    return JSON.parse(JSON.stringify(INITIAL_TEAM_DATA));
}

// 将函数暴露给全局
window.loadTeamData = loadTeamData;
window.getInitialTeamData = getInitialTeamData;

